    def _write_script(self, content: str, output_path: Path) -> Path:
        """
        Write R script to file.

        Args:
            content: R script content
            output_path: Path for output file

        Returns:
            Path to written file
        """
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        with open(output_path, 'w', encoding='utf-8') as f:
            f.write(content)

        log.info(f"Written R script: {output_path}")
        return output_path

    def generate_and_save(self, analysis_type: str,
                          params: Dict[str, Any],
                          output_path: Optional[Path] = None) -> Path:
        """
        Generate R script and save to file.

        Args:
            analysis_type: Type of analysis ("chd", "afc", "similarity", "wordcloud")
            params: Analysis parameters
            output_path: Path for output file (default: based on analysis type)

        Returns:
            Path to generated script
        """
        generators = {
            'chd': self.generate_chd_script,
            'chd_native': self.generate_chd_native_script,
            'chd_reinert_profiles': self.generate_chd_reinert_profiles_script,
            'afc': self.generate_afc_script,
            'afc_clone': self.generate_afc_clone_script,
            'afc_from_chd': self.generate_afc_from_chd_script,
            'similarity': self.generate_similarity_script,
            'wordcloud': self.generate_wordcloud_script,
            'wordcloud_class': self.generate_wordcloud_class_script,
            'emotions': self.generate_emotions_script,
        }

        analysis_type = analysis_type.lower()
        if analysis_type not in generators:
            raise RScriptGeneratorError(
                what=f"Tipo de análise desconhecido: {analysis_type}",
                why=f"Os tipos suportados são: {', '.join(generators.keys())}",
                how="Use um dos tipos de análise suportados."
            )

        content = generators[analysis_type](params)

        if output_path is None:
            pathout = Path(params.get('pathout', '.'))
            output_path = pathout / f"{analysis_type}_script.R"

        return self._write_script(content, output_path)

